// Process all UI components to fix common syntax errors
function processUIComponents(dir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    
    if (entry.isFile() && 
        (entry.name.endsWith('.ts') || entry.name.endsWith('.tsx'))) {
      try {
        let content = fs.readFileSync(fullPath, 'utf8');
        let original = content;
        
        // Fix React.forwardRef<; pattern
        content = content.replace(/React\.forwardRef<\s*;/g, 'React.forwardRef<');
        
        // Fix <ComponentName; patterns
        content = content.replace(/<([a-zA-Z]+)\s*;/g, '<$1');
        
        // Fix trailing commas in return statements
        content = content.replace(/return\s+(\w+)\s*,\s*\}/g, 'return $1; }');
        
        // Fix Progress component usage
        content = content.replace(/<Progress\s*;/g, '<Progress');
        
        // Fix className concatenation
        content = content.replace(/const className\s*=\s*{([^}]+)}/g, 'className={$1}');
        
        // Fix value assignments
        content = content.replace(/const value\s*=\s*{([^}]+)}/g, 'value={$1}');
        
        if (content !== original) {
          fs.writeFileSync(fullPath, content, 'utf8');
          console.log(`âœ“ Fixed ${entry.name}`);
        }
      } catch (error) {
        // Ignore read errors
      }
    }
  }
}

